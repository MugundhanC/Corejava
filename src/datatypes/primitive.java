package datatypes;

public class primitive {

	public static void main(String[] args) {
		byte b=125;//8 bits
		System.out.println("the byte value is--->"+b);
		short s=1435;//16 bits
		System.out.println("the short value is--->"+s);
		int i=345267;//32 bits
		System.out.println("the int value is--->"+i);
		long l=89760423L;//64 bits
		System.out.println("the long value is--->"+l);
		float f=234.567F;
		System.out.println("the float value is--->"+f);
		double d1=657867;
		System.out.println("the double value is--->"+d1);
		double d2=657.234;
		System.out.println("the double value is--->"+d2);
		boolean b1=true;
		System.out.println("the boolean value is--->"+b1);
		char c='@';
		System.out.println("the charvalue is--->"+c);
	}

}
